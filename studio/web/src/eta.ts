export function estimateRemainingMinutes(progress: string | undefined): number | null {
  if (!progress) return null;
  for (const [prefix, minutes] of PHASE_ETA_MINUTES) {
    if (progress.startsWith(prefix)) return minutes;
  }
  return null;
}
